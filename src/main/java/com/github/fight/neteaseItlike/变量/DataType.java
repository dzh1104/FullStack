package com.github.fight.neteaseItlike.变量;

// 数据类型
public class DataType {
    public static void main(String[] args) {
        // 变量的本质：是在内存当中，开辟了一块空间，专门给你存指定值
        // 数据类型规定了：内存空间当中只能存放什么类型的数据
        int age = 18;
        System.out.println(age);
        age = 20;
        System.out.println(age);
        // age = 18.5;


    }
}
